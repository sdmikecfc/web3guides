/**
 * Predictions login landing — shown when /api/predictions/enter rejects a code.
 * Tells the user what went wrong and how to recover (run `!prediction enter`
 * in Discord to get a fresh link).
 */

export const dynamic = "force-dynamic";
export const runtime  = "nodejs";

const REASONS: Record<string, { title: string; body: string }> = {
  "missing-code":  { title: "Missing access code",
                     body: "The link you clicked didn't include the access code. Run `!prediction enter` in #mini-games on Discord to get a fresh link." },
  "missing-round": { title: "Missing round id",
                     body: "The link didn't include the round id. Run `!prediction enter` in #mini-games on Discord to get a fresh link." },
  "invalid-code":  { title: "Invalid link",
                     body: "This link isn't recognized. It may have been generated by a different user or already been replaced by a newer one. Run `!prediction enter` again to get a fresh link." },
  "already-used":  { title: "Link already used",
                     body: "This link has already been claimed. Each link is single-use. Run `!prediction enter` in Discord to get a fresh one." },
  "expired":       { title: "Link expired",
                     body: "This link expired (links live ~10 minutes for security). Run `!prediction enter` in Discord to get a fresh link." },
  "server-error":  { title: "Something went wrong",
                     body: "Our server hit an error processing your link. Please try `!prediction enter` again in Discord. If the issue persists, ping a Core Team member." },
};

export default function PredictionsLoginPage({
  searchParams,
}: { searchParams: { error?: string } }) {
  const err  = searchParams?.error || "";
  const meta = REASONS[err] || {
    title: "Sign in",
    body: "Run `!prediction enter` in #mini-games on Discord to get your access link.",
  };

  return (
    <main style={{
      minHeight: "100vh",
      display: "flex",
      alignItems: "center",
      justifyContent: "center",
      background: "linear-gradient(135deg, #070b1a 0%, #0a0e27 50%, #1a0b3d 100%)",
      color: "#f8fafc",
      fontFamily: "Inter, system-ui, sans-serif",
      padding: "32px",
    }}>
      <div style={{
        maxWidth: 520,
        background: "rgba(13,17,32,0.7)",
        border: "1px solid rgba(124,106,255,0.25)",
        borderRadius: 16,
        padding: "40px 36px",
      }}>
        <div style={{
          fontSize: 12, color: "#f0b340", letterSpacing: 4,
          textTransform: "uppercase", fontWeight: 700, marginBottom: 14,
        }}>
          Doma Predictions
        </div>
        <h1 style={{ fontSize: 28, fontWeight: 800, margin: 0, marginBottom: 14 }}>
          {meta.title}
        </h1>
        <p style={{ fontSize: 16, color: "#cbd5e1", lineHeight: 1.55, margin: 0 }}>
          {meta.body}
        </p>
        <div style={{
          marginTop: 28, padding: "14px 16px",
          background: "rgba(124,106,255,0.08)",
          border: "1px solid rgba(124,106,255,0.25)",
          borderRadius: 8,
          fontFamily: "ui-monospace, SFMono-Regular, Menlo, monospace",
          fontSize: 14, color: "#a78bfa",
        }}>
          !prediction enter
        </div>
      </div>
    </main>
  );
}
